#include <stdio.h>
#include <stdlib.h>

void main() {
    int a = 5, b = 4;

    printf(" Soma de a (%d) e b (%d): %d", a, b, a + b);
    printf("\n Subtração de a (%d) e b (%d): %d", a, b, a - b);
    printf("\n Divisão de a (%d) e b (%d): %d", a, b, a / b);
    printf("\n Multiplicação de a (%d) e b (%d): %d", a, b, a * b);
    printf("\n Resto da divisão de a (%d) e b (%d): %d", a, b, a % b);
    printf("\n Valor absoluto -1: %d", abs(-1));
}
